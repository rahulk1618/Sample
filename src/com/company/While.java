package com.company;

public class While {
    public static void main(String[] args){
        int i =0;
        int j=0;
        int k =0;
        while (i<=5){
            System.out.println(i);
            i++;
        }
        do{
            System.out.println(j);
            j++;
        } while ( j<=5);
        for (k=0;k<=5; k++){
            System.out.println(k);
        }
    }
}
