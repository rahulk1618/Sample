package com.company;

public class SwitchExample {
    public static void main(String[] args){
        int i= 5;
        switch(i){
            case 1:   {
                     System.out.println("f off");
                     break;
                      }
            case 2: {
                     System.out.println("go f off");
                     break;
                    }
            case 5:  {
                     System.out.println("case 5");
                     break;
                     }
             default:{
                 {
                     System.out.println("go f off default");
                     break;
                 }
             }

        }
    }
}
